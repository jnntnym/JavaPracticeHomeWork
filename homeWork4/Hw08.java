/*
 *Q:8 -Create a method and implement for loop concept inside of the method and call the method in the main method
 */
public class Hw08 {
	public static void loop() {

		for (int i = 0; i <= 10; i++) { // print 0 to 10
			System.out.println(i);
		}
	}
	public static void main(String[] args) {
		loop();
	}

}
